class Solution 
{
    public int[] twoSum(int[] nums, int target)
    {
        for(int j=0;j<nums.length;j++)
        {
            for(int i=j+1;i<nums.length;i++)
            {
                if(nums[i]+nums[j]==target)
                    return new int[]{j,i};
            }

        }
        return new int[]{-1,-1};

    }
}
